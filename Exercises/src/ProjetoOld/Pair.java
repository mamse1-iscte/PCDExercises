package ProjetoOld;

public class Pair<T1, T2> {

	T1 f1;
	T2 f2;
	
	public Pair(T1 f1, T2 f2) {
		this.f1 = f1;
		this.f2 = f2;
	}

	public T1 getF1() {
		return f1;
	}

	public void setF1(T1 f1) {
		this.f1 = f1;
	}

	public T2 getF2() {
		return f2;
	}

	public void setF2(T2 f2) {
		this.f2 = f2;
	}
	
	
	
	
}
